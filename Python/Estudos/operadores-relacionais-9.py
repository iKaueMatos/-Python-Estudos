#OPERADORES RELACIONAIS

# == igualdade
#  > variavel 1 e maior que variavel 2 ?
# < variavel 1 e menor que variavel 2 ?
# => variavel 1 e maior ou igual a variavel 2?
# <= variavel 1 e menor ou igual a variavel 2 ?
#! variavel 1 e diferente da variavel 2?

#igualdade

number = 1
number2 = 1

expressao = number == number2

print("igualdade",expressao)

# maior >

number3 = 1
number4 = 2

expressao = number3 > number4

print("maior",expressao)

# menor <

number5 = 1
number6 = 0

expressao = number5 < number6

print("menor",expressao)

# menor ou igual <=

number7 = 1
number8 = 0

expressao = number7 <= number8

print("menor ou igual",expressao)

# maior ou igual =>

number9 = 3
number10 = 4

expressao = number9 <= number10

print("maior ou igual",expressao)


# diferente !=

number11 = 1
number12 = 0

expressao = number11 <= number12

print("Diferente",expressao)