#Como podemos inserir um item no começo da lista?


#Lista gerada pelo range maneira mais convecional e rapida para gerar uma lista!
lista = list(range(1,10))

#Basicamente dessa forma conseguimos falar a posiçao que queremos o elemento e ao mesmo tempo adicionalo a lista
lista.insert(10,1)
#          posição,ADD Elemento para ser adicionado

print(lista)